import json
import os
import argparse

def show_task(file_path, task_id):
    if not os.path.exists(file_path):
        print(f"Error: File {file_path} not found.")
        return

    with open(file_path, 'r') as f:
        data = json.load(f)

    results = data.get('results', [])
    task = next((r for r in results if r['task_id'] == task_id), None)

    if not task:
        print(f"Error: Task {task_id} not found in {file_path}.")
        return

    print("="*80)
    print(f"DEMO: Task {task_id} - End-to-End Evaluation")
    print("="*80)
    
    print("\n[1. TASK PROMPT]")
    print("-" * 20)
    # Print first 200 chars and last 200 chars to show the context
    prompt = task.get('prompt', '')
    if len(prompt) > 400:
        print(prompt[:200] + "\n... [truncated] ...\n" + prompt[-200:])
    else:
        print(prompt)

    print("\n[2. MODEL INPUT (TRUNCATED)]")
    print("-" * 20)
    model_input = task.get('model_input', '')
    # Usually contains system prompt + user prompt. We just show the end of it.
    print("... " + model_input[-300:] if len(model_input) > 300 else model_input)

    print("\n[3. CANDIDATE OUTPUT (GENERATED BY MODEL)]")
    print("-" * 20)
    print(task.get('candidate_text', ''))

    print("\n[4. SCORING RESULTS]")
    print("-" * 20)
    score_data = task.get('score', {})
    print(f"Overall Score: {score_data.get('overall_score', 'N/A')}")
    print("\nComponent Scores:")
    for comp, val in score_data.get('component_scores', {}).items():
        print(f"  - {comp:35}: {val}")

    print("\n[5. EVALUATOR FEEDBACK (WHY IT SCORED THIS WAY)]")
    print("-" * 20)
    details = score_data.get('details', {})
    
    # Custom logic to highlight common failures in the demo task
    if task_id == "tb_manual_0027":
        print("Note: The model generated a 'job post' style message instead of a sales email.")
        print("This resulted in:")
        print("  - signal_grounding: 0 (No specific mention of the hiring signal in a sales context)")
        print("  - tenacious_tone_style: 1 (Marketing/Hypy tone instead of grounded Tenacious tone)")
        print("  - segment_fit: 1 (Failure to address the prospect as a potential buyer)")

    print("\n" + "="*80)

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Show a single task's evaluation result for demo purposes.")
    parser.add_argument("--file", default="ablations/path_b_orpo_full_v3_eval_held_out.json", help="Path to the JSON results file")
    parser.add_argument("--task", default="tb_manual_0027", help="Task ID to display")
    args = parser.parse_args()
    
    show_task(args.file, args.task)
